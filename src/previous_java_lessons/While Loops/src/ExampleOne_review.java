
public class ExampleOne_review {
	
	public static void main(String[] args) {
		
		int counter=1;
		
		while(counter<=5) {
			System.out.println(counter);
			counter++;
		}
		
		int i=1;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		
		int pos=100;
		while(pos>=-100) {
			System.out.println(pos);
			pos-=2;
		}
	}
}
